<template>  
	<div v-if="!preview">
		<el-select 
		  v-if="record.options.multiple"
        v-model="models[record.model]"
        :value-key="itemProp.value"
        :style="`width:${record.options.width}`"
       
        :remote="record.options.onlineSearch && record.options.showSearch"
        :remote-method="remoteMethod"
        :placeholder="record.options.placeholder"
        :filterable="record.options.showSearch"
        :disabled="recordDisabled"
        :clearable="record.options.clearable"
        multiple
        @change="handleChange($event)" 
        @clear="clearChange" 
        @focus="handleFocus"
        @blur="handleBlur"
      >
        <template  v-for="(item, index) in ((record.options.dynamic == 1 && record.options.remoteFunc) || (record.options.dynamic == 2 && record.options.dictType) ? checkValues : record.options.options)">
          <el-option 
            :key="item[itemProp.value] + index"
            :label="item[itemProp.label]"
            :value="item[itemProp.value]"
            v-if="itemVisible(item)"
            >
          </el-option>
        </template>
      </el-select>
      <el-select
        v-else 
        v-model="models[record.model]"
        :style="`width:${record.options.width}`"
        :value-key="itemProp.value" 
        :remote="record.options.onlineSearch && record.options.showSearch"
        :remote-method="remoteMethod"
        :placeholder="record.options.placeholder"
        :filterable="record.options.showSearch"
        :disabled="recordDisabled"
        :clearable="record.options.clearable" 
        @change="handleChange($event)" 
        @clear="clearChange" 
        @focus="handleFocus"
        @blur="handleBlur"
      > 
        <template v-for="(item, index) in ((record.options.dynamic == 1 && record.options.remoteFunc) || (record.options.dynamic == 2 && record.options.dictType) ? checkValues : record.options.options)">
          <el-option
            :key="item[itemProp.value] + index"
            :label="item[itemProp.label]"
            :value="item[itemProp.value]"
            v-if="itemVisible(item)"
            >
          </el-option>
        </template> 
      </el-select> 
	</div>
  <span v-else>
    {{models[record.model+'_label']}}  
  </span>
</template>
<script>
import mixin from './datasource-mixin.js'
export default {
	mixins: [mixin],
	data() {
		return {
			itemProp: {
		        children: 'children',
		        value: 'value',
		        label: 'label',
		        multiple: this.record.options.multiple 
		  },
      // 2021-03-13 如果该字段带有本地数据过滤,则这里保存本地过滤的过滤条件
      localFilter: [],
		}
	},
	created () { 
	  //this.updateSimpleDefaultValue()
    if(!this.record.options) return 
    // 初始化一个绑定空值 
    if(this.record.options.multiple) {
      this.updateArrayDefaultValue()
    } else {
      this.updateSimpleDefaultValue()
    }


    // 判断如果是远程方法的话 远程请求数据
    this.initDynamicValue()



    // 判断如果已经有了值 但没有label 则尝试回填label 
    const value = this.models[this.record.model]
    if(value && value.length > 0) {
      this.handleChange(value)
    }
	},
  methods: {
     // select 清除后回调
    clearChange() {
      // 2021-05-08 lyf 判断是否有清除后回调
      if(!this.record.options.clearCb) {
        return
      }

      const cbScript = this.record.options.clearCb
      const func =  '{' + cbScript + '}'
      const Fn = new Function('$' , 'data', func)
      Fn(this.models, this.data)
    }
  }
}
</script>